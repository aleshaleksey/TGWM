///
///Types include:
///-Lifeform: handles the stats of monsters and players.
///-Shade: handles stat changes of Lifeforms in battle.
///-Spell: handles base stats of spells.
///-Place: handles loci (affinity, monster populations, encounter specs.)
///-Item: equipment UNIMPLEMENTED
///-Landscapes: backgrounds for different types of places in battle.
///-Dungeon: A microcosm of locations, custom monsters and story.
