impl Lifeform {
	
	pub fn rename(mut self,new_name:&'static str)->Self {
		self.name = new_name;
		self
	}
	
	pub fn spellist(mut self,new_spellist:Vec<&'static str>)->Self {
		self.Spellist = new_spellist;
		self
	}
	
	pub fn speed_change(mut self,factor:f32)->Self {
		self.Speed = self.Speed*factor;
		self.Speed_shade = self.Speed_shade*factor;
		self
	}	
	
	pub fn hp_change(mut self,factor:f32)->Self {
		self.HP = self.HP*factor;
		self.HP_shade = self.HP_shade*factor;
		self
	}
		
	pub fn mp_change(mut self,factor:f32)->Self {
		self.MP = self.MP*factor;
		self.MP_shade = self.MP_shade*factor;
		self
	}
	
	pub fn attack_change(mut self,factor:f32)->Self {
		self.Attack = self.Attack*factor;
		self.Attack_shade = self.Attack_shade*factor;
		self
	}
	
	pub fn defence_change(mut self,factor:f32)->Self {
		self.Defence = self.Defence*factor;
		self.Defence_shade = self.Defence_shade*factor;
		self
	}
	
	pub fn wm_change(mut self,factor:f32)->Self {
		self.WM = self.WM*factor;
		self.WM_shade = self.WM_shade*factor;
		self
	}
	
	pub fn bm_change(mut self,factor:f32)->Self {
		self.BM = self.BM*factor;
		self.BM_shade = self.BM_shade*factor;
		self
	}
	
	pub fn re_type(mut self,new_type:u8)->Self {
		self.Type = new_type;
		self.Alive = true;
		self.Unclean = false;
		if (new_type==ANGEL) | (new_type==UNDEAD) | (new_type==ALIEN) {self.Unclean = true;};
		if new_type==UNDEAD {
			self.Alive = false;
			self.Unclean = true;				
		};	
		self
	}
	
	
	//NB Mirror does not mirror affinity or Exp.
	//NB2 This is an evil mirror of evil which consumes the lifeform it is fed. So you may want to feed it a clone.
	pub fn mirror(mut self,original:Lifeform)->Self {
		self.Spellist = Vec::new();
		for i in 0..original.Spellist.len() {
			self.Spellist.push(original.Spellist[i]);
		};
		self.Alive = original.Alive;
		self.Unclean =original.Unclean;
		self.BM = original.BM;
		self.BM_shade = original.BM_shade;
		self.WM = original.WM;
		self.WM_shade = original.WM_shade;
		self.Defence = original.Defence;
		self.Defence_shade = original.Defence_shade;
		self.Attack = original.Attack;
		self.Attack_shade = original.Attack_shade;
		self.MP = original.MP;
		self.MP_shade = original.MP_shade;
		self.HP = original.HP;
		self.HP_shade = original.HP_shade;
		self.Speed = original.Speed;
		self.Speed_shade = original.Speed_shade;
		self
	} 
	
	//difficulty of 0 is the standard setting.
	pub fn diff_lvl(mut self,diff:isize)->Self {
		let factor = diff as f32 / 10.0 + 1.0;
		
		self.Speed = self.Speed*factor;
		self.Speed_shade = self.Speed_shade*factor;
		self.HP = self.HP*factor;
		self.HP_shade = self.HP_shade*factor;
		self.MP = self.MP*factor;
		self.MP_shade = self.MP_shade*factor;
		self.Attack = self.Attack*factor;
		self.Attack_shade = self.Attack_shade*factor;
		self.Defence = self.Defence*factor;
		self.Defence_shade = self.Defence_shade*factor;
		self.WM = self.WM*factor;
		self.WM_shade = self.WM_shade*factor;
		self.BM = self.BM*factor;
		self.BM_shade = self.BM_shade*factor;
		self.Exp = self.Exp*factor;
		self.ExpUsed = self.ExpUsed*factor;
		self
	}		
}



