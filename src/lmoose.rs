#[allow(unused_must_use)]
impl Display for Place{
 fn fmt(&self, f: &mut Formatter)->fmt::Result {
  let n_match= if self.xy[1]>70{"in the far north"
			   }else if self.xy[1]>30{"in the north"
			   }else if self.xy[1]>0{"in the tropics"
			   }else if self.xy[1]>-30{"in the tropics"
			   }else if self.xy[1]<(-30){"in the south"
			   }else{"in the deep south"};
			   
  let e_match= if self.xy[0]>40{" to the east of Malachia"
			   }else if self.xy[0]>120{" far to the east of Malachia"
			   }else if self.xy[0]<(-140){" far to the east of Malachia"
			   }else if self.xy[0]<(-20){" far to the west of Malachia"
			   }else if self.xy[0]<40{" to the west of Malachia"
			   }else{""};
			   
  let scaper= match self.scape{
	  VOID=>"twisted expanse of corrupted spacetime",
	  ICE=>"icy wasteland",
	  _=>convert_terrain(self.scape),
  }; 
	  
  let mut pops:Vec<u8>= Vec::new();
  for x in self.popu.iter(){
	  if x.2>499{pops.push(x.1)}else{}
  };
  pops = uniq_m(pops);
  
  write!(f,"\nThis place is {}...\n",self.name);
  write!(f,"...This {} is located {}{}.\n",scaper,n_match,e_match);
  let pl= if pops.len()>0 {pops.len()-1}else{0};
  if pops.len()==1 {
	  write!(f,"It is populated by");
	  if pops[0]==UNDEAD{
		  write!(f," undead.");
	  }else{
		  write!(f," {}s.",convert_mon_type(pops[0]));
   };
  }else if pops.len()>1{
   //let mut lost:usize=0;
	   for i in 0..pops.len(){
		   if i==0{write!(f,"...It is populated by");};			
		   if i<pl {
			if pops[i]==UNDEAD{
				write!(f," undead,");
			}else{
				write!(f," {}s,",convert_mon_type(pops[i]));
			};
		   }else{
			   if pops[pops.len()-1]==UNDEAD{
				   write!(f," and undead");
			   }else{
				   write!(f," and {}s.",convert_mon_type(pops[pops.len()-1]));
			   };
		   };
		};
  }else{write!(f,"...It is unpopulated");};
  write!(f,"\n\n")
 }
} //END IMPL DISPLAY PLACE

#[allow(unused_must_use)]
impl Display for Spell{
 fn fmt(&self, f: &mut Formatter)->fmt::Result {
  let dam_t= if self.Type==HEALING {"healing potential"}
			 else if self.Light		 {"potential"}
             else 					 {"destructive potential"};
  let dam= if (self.Power>=0.0) & (self.Power<10.0) {
	  "a modest"
  }else if (self.Power>=10.0) & (self.Power<20.0) {
	  "a moderate"
  }else if (self.Power>=20.0) & (self.Power<30.0) {
   "a great"
  }else if (self.Power>=30.0) & (self.Power<40.0) {
   "an immense"
  }else if (self.Power>=40.0) & (self.Power<50.0) {
	  "a deific"
  }else{
	  "an unfathomable"
  };
  let mp_i= if self.MP<10.0 {
	  "a little mana"
  }else if (self.MP>=10.0) & (self.MP<40.0) {
	  "some mana"
  }else if (self.MP>=40.0) & (self.MP<100.0) {
	  "a fair flow of mana"
  }else if (self.MP>=100.0) & (self.MP<240.0) {
	  "a lot of mana"
  }else if (self.MP>=240.0) & (self.MP<500.0) {
	  "more mana than most can muster"
  }else{
	  "immeasurable torrents of mana"
  };
  let typ= if self.Light
   {"White Magic"} else {"Black Magic"};
  let targ_boon= match self.target_boon{
   SINGLE => "a single being the caster wishes",
   SELF => "only the caster",
   TARGET=> "the one the caster wishes",
   GROUP|GROUPS => "a group the caster targets",
   PARTY => "the caster's party",
   ALL => "anything and everything",
   ANY => "all those the caster wishes",
   _ => "no one",
};
  let targ_woe= match self.target_woe{
   SINGLE => "a single being the caster wishes to harm",
   SELF => "only the caster",
   TARGET=> "the one the caster wishes",
   GROUP|GROUPS => "a group the caster targets",
   PARTY => "the caster's party",
   ALL => "anything and everything",
   ANY => "any to whom the caster wishes harm",
   SAME => targ_boon,
   _ => "no one",
};
  write!(f,"{} is {}...\n",self.name,typ);
  write!(f,"...It is a {} invocation that takes {} to cast and has {} {}.\n",self.Type,mp_i,dam,dam_t);
  write!(f,"...It smites {}...\n", targ_woe);
  write!(f,"...And aids {}...\n", targ_boon);
  match self.Death{
   true => write!(f,"{} calls upon forces beyond the pale to wisk away the target's life force to the otherworld.\n", self.name),
   false => write!(f,""),
};
  
  match self.Teleport{
   true => write!(f,"{} warps {} to the place envisioned in the mind's eye.\n", self.name, targ_boon),
   false =>write!(f,""),
};
  let light_text=if (self.Light==true) & (self.Type==RADIANT)
   {"evokes light."}
  else if (self.Light==false) & (self.Type==RADIANT)
   {"extinguishes light"}
  else{""};

  match self.Type {
   RADIANT=>write!(f,"{} {}.\n",self.name,light_text),
   _=>write!(f,""),
};
  let time_text=if self.Timestop==true
   {"can induce time to stop"}else{""};
  if self.Timestop==true{write!(f,"{} {}.\n",self.name,time_text)}
  else{write!(f,"")}

 }
}

#[allow(unused_must_use)]
impl Display for Lifeform{
 fn fmt(&self, f: &mut Formatter)->fmt::Result {

  let sp_val=(self.Spellist.len()*250) as f32;
  let lf_eval=(self.HP+self.MP+self.Speed*10.0+(self.Attack+self.Defence+self.WM+self.BM)*5.0+sp_val)/2500.0;
  let lf_danger=
   if lf_eval<0.5 {"weak "}
   else if lf_eval<1.0{""}
   else if lf_eval<1.5 {"quite powerful "}
   else if lf_eval<2.0{"powerful "}
   else {"dangerous "};

  let _lf_undead=
   if self.Alive==false {", undead "}
   else {" "};
  
  let lf_sp_feather=self.Spellist.len() as i32;
  let lf_sp_is= if lf_sp_feather==0 {" do not"} else {""};

  let lf_l= self.Spellist.len();
  let lf_sp_num= match lf_l {
   0=>"",
   1=>" a little",
   2...3=>" some",
   4...8=>" much",
   _=>"",
  };
  
  let lf_sp_pow= self.MP*(self.WM+self.BM)/2500.0;
  let lf_sp_pow=
   if lf_l==0{""}
   else if lf_sp_pow<0.25 {" of negligeable potence"}
   else if lf_sp_pow<1.0 {" of little power"}
   else if lf_sp_pow<10.0 {""}
   else if lf_sp_pow<50.0 {" of some power"}
   else if lf_sp_pow<500.0 {" of great power"}
   else {" of unimaginable potence"};

  let lf_res= match self.HP as i64{
   0...99=>"no ",
   100...199=>"little ",
   200...399=>"some ",
   40...799=>"",
   80...1699=>"great ",
   _=>"ungodly ",
  };

  let lf_attack= match self.Attack as i64{
   0...24=>"feeble",
   25...49=>"weak",
   50...99=>"average",
   100...199=>"dangerous",
   200...399=>"very dangerous",
   _=>"devastating",
  };

  let lf_defence= match self.Defence as i64{
   0...24=>"impotent",
   25...49=>"shaky",
   50...99=>"mediocre",
   100...199=>"worthy",
   200...399=>"impressive",
   _=>"nigh-impenetrable",
  };

  let lf_speed= match self.Speed as i64{
   0...19=>"are slow, ",
   20...39=>"are quite slow, ",
   40...59=>"",
   60...79=>"are quite fast, ",
   80...99=>"are fast, ",
   _=>"are lightning fast, ",
  };

  let lf_type=
   if self.Alive==true {[convert_mon_type(self.Type),"s"].join("")}
   else{[convert_mon_type(self.Type),""].join("")};

  let lf_name=match self.name{
   "Witch"=>"Witche",
   "White Witch"=>"White Witche",
   "Goblin Witch"=>"Goblin Witche",
   _=>self.name,
  };

  write!(f,"\n{}s are {}{}, which are fond of {}.\n",lf_name,lf_danger,lf_type,convert_affinity(self.Affinity));

  write!(f,"...{}s {}have {}resilence, {} attacks and {} defence.\n",lf_name, lf_speed, lf_res, lf_attack, lf_defence);
  write!(f,"They{} know{} arcana{}.\n",lf_sp_is,lf_sp_num,lf_sp_pow);
  if self.Unclean==true{write!(f,"...They are unclean.\n")} else{write!(f,"")};
  write!(f,"\n")
 }
}

